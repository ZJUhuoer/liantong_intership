const provinceSelect = document.getElementById("province");
const citySelect = document.getElementById("city");
const districtSelect = document.getElementById("district");
const cityContainer = document.getElementById("city-container");
const districtContainer = document.getElementById("district-container");
const searchBtn = document.getElementById("search-btn");

// 当选择省份时
provinceSelect.addEventListener("change", () => {
    if (provinceSelect.value === "浙江") {
        cityContainer.classList.remove("hidden");
    } else {
        cityContainer.classList.add("hidden");
        districtContainer.classList.add("hidden");
        citySelect.value = "";
        districtSelect.value = "";
    }
});

// 当选择市时
citySelect.addEventListener("change", () => {
    const selectedCity = citySelect.value;
    if (selectedCity && selectedCity !== "全部") {
        districtContainer.classList.remove("hidden");
        const districts = cityDistrictData[selectedCity] || [];
        districtSelect.innerHTML = "<option value=''>请选择区/县</option>";
        districts.forEach(d => {
            const option = document.createElement("option");
            option.value = d;
            option.textContent = d;
            districtSelect.appendChild(option);
        });
    } else {
        districtContainer.classList.add("hidden");
        districtSelect.value = "";
        districtSelect.innerHTML = "<option value=''>请选择区/县</option>";
    }
});

// 点击查询按钮
searchBtn.addEventListener("click", () => {
    const keyword = document.getElementById("keyword").value.trim();
    const province = provinceSelect.value;
    const city = citySelect.value;
    const district = districtSelect.value;

    const resultPanel = document.getElementById("result-panel");

    if (keyword === "主营收入" && province === "全部") {
        resultPanel.innerHTML = getAllNationTable1();
    } else if (keyword === "主营收入" && province === "浙江" && city === "杭州" && district === "拱墅区") {
        resultPanel.innerHTML = getGongshuTable();
    } else if (keyword === "主营收入" && province === "浙江" && city === "杭州") {
        resultPanel.innerHTML = getHangzhouTable1();
    } else if (keyword === "主营收入" && province === "浙江") {
        resultPanel.innerHTML = getZhejiangTable1();
    } else if (keyword === "主营收入") {
        resultPanel.innerHTML = showThreeBlocks();
    } else {
        resultPanel.innerHTML = "<p>暂无数据</p>";
    }
});

